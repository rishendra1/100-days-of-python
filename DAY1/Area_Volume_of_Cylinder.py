#Cylinder
radius = int(input("Enter radius of cylinder: "))
height = int(input("Enter height of cylinder: "))
print("c.s.a of cylinder is -", 2*3.14*radius*height)
print("t.s.a of cylinder is -", 2*3.14*radius*(radius + height))
print("Volume is -", 3.14 * radius *radius * height)
